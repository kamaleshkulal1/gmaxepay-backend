/**
 * Assign Slab to Company (Super Admin only)
 */
const assignSlabToCompany = async (req, res) => {
  try {
    if (req.user?.userType !== USER_TYPES.SUPER_ADMIN) {
      return res.failure({ message: 'Only Super Admin can assign slabs to companies' });
    }

    const { slabId, companyId } = req.body;

    if (!slabId || !companyId) {
      return res.failure({ message: 'Slab ID and Company ID are required' });
    }

    // Verify slab exists
    const slab = await dbService.findOne(model.slab, { id: slabId });
    if (!slab) {
      return res.failure({ message: 'Slab not found' });
    }

    // Verify company exists
    const company = await dbService.findOne(model.company, { id: companyId });
    if (!company) {
      return res.failure({ message: 'Company not found' });
    }

    // Get all operators to create admin commission entries
    const operators = await dbService.findAll(
      model.operator,
      {},
      { select: ['id', 'operatorName', 'operatorType'] }
    );

    // Create admin commission entries for this slab-company combination
    const commissionData = [];
    for (const operator of operators) {
      // Commission entry
      commissionData.push({
        slabId,
        companyId,
        operatorId: operator.id,
        operatorName: operator.operatorName,
        operatorType: operator.operatorType,
        commAmt: 0,
        commType: 'com',
        amtType: 'fix',
        isActive: true,
        addedBy: req.user.id
      });

      // Surcharge entry
      commissionData.push({
        slabId,
        companyId,
        operatorId: operator.id,
        operatorName: operator.operatorName,
        operatorType: operator.operatorType,
        commAmt: 0,
        commType: 'sur',
        amtType: 'fix',
        isActive: true,
        addedBy: req.user.id
      });
    }

    // Check if commissions already exist
    const existingCommissions = await dbService.findAll(model.adminCommission, {
      slabId,
      companyId
    });

    if (existingCommissions.length === 0) {
      await dbService.createMany(model.adminCommission, commissionData);
    }

    return res.success({
      message: 'Slab assigned to company successfully',
      data: { slabId, companyId }
    });
  } catch (error) {
    console.error(error);
    return res.internalServerError({ message: error.message });
  }
};

