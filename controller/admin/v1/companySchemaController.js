/**
 * Get Commissions by Schema
 */
const getCommissionsBySchema = async (req, res) => {
  try {
    let permissions = req.permission || [];
    let hasPermission = permissions.some(
      (permission) =>
        permission.dataValues.permissionId === 27 &&
        permission.dataValues.read === true
    );

    if (req.user?.userType === USER_TYPES.SUPER_ADMIN) {
      hasPermission = true;
    }

    if (!hasPermission) {
      return res.failure({ message: `User doesn't have Permission!` });
    }

    const { schemaId, roleType } = req.query;
    const companyId = req.user?.companyId;

    if (!schemaId) {
      return res.failure({ message: 'Schema ID is required' });
    }

    let query = { schemaId };
    if (companyId && req.user?.userType !== USER_TYPES.SUPER_ADMIN) {
      query.companyId = companyId;
    }

    if (roleType) {
      query.roleType = roleType;
    }

    // Determine which commission table to query based on roleType
    let commissionModel;
    if (roleType === 3) {
      commissionModel = model.masterDistributorCommission;
    } else if (roleType === 4) {
      commissionModel = model.distributorCommission;
    } else if (roleType === 5) {
      commissionModel = model.retailerCommission;
    } else {
      commissionModel = model.companyCommission;
    }

    const commissions = await dbService.findAll(commissionModel, query, {
      include: [
        { model: model.operator, as: 'operator' },
        { model: model.schema, as: 'schema' }
      ],
      order: [['id', 'ASC']]
    });

    return res.success({
      message: 'Commissions retrieved successfully',
      data: commissions
    });
  } catch (error) {
    console.error(error);
    return res.internalServerError({ message: error.message });
  }
};

/**
 * Get User Commissions
 */
const getUserCommissions = async (req, res) => {
  try {
    let permissions = req.permission || [];
    let hasPermission = permissions.some(
      (permission) =>
        permission.dataValues.permissionId === 27 &&
        permission.dataValues.read === true
    );

    if (req.user?.userType === USER_TYPES.SUPER_ADMIN) {
      hasPermission = true;
    }

    if (!hasPermission) {
      return res.failure({ message: `User doesn't have Permission!` });
    }

    const userId = req.params.userId;
    const companyId = req.user?.companyId;

    // Get user details
    const user = await dbService.findOne(model.user, {
      id: userId,
      companyId
    });

    if (!user) {
      return res.failure({ message: 'User not found' });
    }

    // Get user's schema
    const schema = await dbService.findOne(model.schema, {
      id: user.schemaId
    });

    if (!schema) {
      return res.success({
        message: 'User has no schema assigned',
        data: []
      });
    }

    // Get commissions based on user role
    let commissions = [];
    if (user.userRole === USER_TYPES.MASTER_DISTRIBUTOR) {
      commissions = await dbService.findAll(model.masterDistributorCommission, {
        schemaId: schema.id,
        masterDistributorId: userId
      }, {
        include: [{ model: model.operator, as: 'operator' }]
      });
    } else if (user.userRole === USER_TYPES.DISTRIBUTOR) {
      commissions = await dbService.findAll(model.distributorCommission, {
        schemaId: schema.id,
        distributorId: userId
      }, {
        include: [{ model: model.operator, as: 'operator' }]
      });
    } else if (user.userRole === USER_TYPES.RETAILER) {
      commissions = await dbService.findAll(model.retailerCommission, {
        schemaId: schema.id,
        retailerId: userId
      }, {
        include: [{ model: model.operator, as: 'operator' }]
      });
    } else {
      commissions = await dbService.findAll(model.companyCommission, {
        schemaId: schema.id,
        assignedToUserId: userId
      }, {
        include: [{ model: model.operator, as: 'operator' }]
      });
    }

    return res.success({
      message: 'User commissions retrieved successfully',
      data: {
        user: {
          id: user.id,
          name: user.name,
          userRole: user.userRole
        },
        schema: {
          id: schema.id,
          schemaName: schema.schemaName,
          schemaType: schema.schemaType
        },
        commissions
      }
    });
  } catch (error) {
    console.error(error);
    return res.internalServerError({ message: error.message });
  }
};

