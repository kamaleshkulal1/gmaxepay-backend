/**
 * Auto-generate Good and Bad schemas for a user
 */
const autoGenerateDefaultSchemas = async (companyId, createdBy, roleType, roleName) => {
  const defaultSchemas = [
    {
      companyId,
      schemaName: 'Good Client',
      schemaType: 'good',
      createdBy,
      roleType,
      roleName,
      isDefault: true,
      isActive: true,
      description: 'Default Good Client schema'
    },
    {
      companyId,
      schemaName: 'Bad Client',
      schemaType: 'bad',
      createdBy,
      roleType,
      roleName,
      isDefault: true,
      isActive: true,
      description: 'Default Bad Client schema'
    }
  ];

  const createdSchemas = [];
  for (const schemaData of defaultSchemas) {
    const existing = await dbService.findOne(model.schema, {
      companyId,
      schemaName: schemaData.schemaName,
      roleType,
      createdBy,
      schemaType: schemaData.schemaType
    });

    if (!existing) {
      const created = await dbService.createOne(model.schema, schemaData);
      createdSchemas.push(created);
    } else {
      createdSchemas.push(existing);
    }
  }

  return createdSchemas;
};

/**
 * Create Schema
 */
const createSchema = async (req, res) => {
  try {
    let permissions = req.permission || [];
    let hasPermission = permissions.some(
      (permission) =>
        permission.dataValues.permissionId === 27 &&
        permission.dataValues.write === true
    );

    if (req.user?.userType === USER_TYPES.SUPER_ADMIN) {
      hasPermission = true;
    }

    if (!hasPermission) {
      return res.failure({ message: `User doesn't have Permission!` });
    }

    const dataToCreate = { ...(req.body || {}) };
    const companyId = req.user?.companyId;

    if (!companyId) {
      return res.failure({ message: 'Company ID is required' });
    }

    // Validate schemaType
    if (dataToCreate.schemaType && !['good', 'bad', 'custom'].includes(dataToCreate.schemaType)) {
      return res.failure({ message: 'schemaType must be "good", "bad", or "custom"' });
    }

    // Set defaults
    if (!dataToCreate.schemaType) {
      dataToCreate.schemaType = 'custom';
    }

    // Map userRole to roleType and roleName
    const roleMapping = {
      [USER_TYPES.SUPER_ADMIN]: { roleType: 1, roleName: 'AD' },
      [USER_TYPES.ADMIN]: { roleType: 2, roleName: 'WU' },
      [USER_TYPES.MASTER_DISTRIBUTOR]: { roleType: 3, roleName: 'MD' },
      [USER_TYPES.DISTRIBUTOR]: { roleType: 4, roleName: 'DI' },
      [USER_TYPES.RETAILER]: { roleType: 5, roleName: 'RE' }
    };

    const userRole = dataToCreate.roleType || req.user.userRole;
    const roleInfo = roleMapping[userRole] || { roleType: userRole, roleName: 'UNKNOWN' };

    dataToCreate.companyId = companyId;
    dataToCreate.createdBy = req.user.id;
    dataToCreate.roleType = roleInfo.roleType;
    dataToCreate.roleName = roleInfo.roleName;
    dataToCreate.isActive = dataToCreate.isActive !== false;

    // Check for duplicate schema name
    const existingSchema = await dbService.findOne(model.schema, {
      companyId,
      schemaName: dataToCreate.schemaName,
      roleType: dataToCreate.roleType,
      createdBy: dataToCreate.createdBy
    });

    if (existingSchema) {
      return res.failure({ message: 'Schema with this name already exists for this role' });
    }

    const createdSchema = await dbService.createOne(model.schema, dataToCreate);

    if (!createdSchema) {
      return res.failure({ message: 'Failed to create schema' });
    }

    return res.success({
      message: 'Schema created successfully',
      data: createdSchema
    });
  } catch (error) {
    console.error(error);
    if (error.name === 'SequelizeUniqueConstraintError') {
      return res.validationError({ message: error.errors[0].message });
    } else {
      return res.internalServerError({ message: error.message });
    }
  }
};

