/**
 * Get All Schemas
 */
const getAllSchemas = async (req, res) => {
  try {
    let permissions = req.permission || [];
    let hasPermission = permissions.some(
      (permission) =>
        permission.dataValues.permissionId === 27 &&
        permission.dataValues.read === true
    );

    if (req.user?.userType === USER_TYPES.SUPER_ADMIN) {
      hasPermission = true;
    }

    const dataToFind = req.body || {};
    const companyId = req.user?.companyId;
    let options = {};
    let query = {};

    if (dataToFind.query) {
      query = { ...query, ...dataToFind.query };
    }

    if (companyId && req.user?.userType !== USER_TYPES.SUPER_ADMIN) {
      query.companyId = companyId;
    }

    if (dataToFind.isCountOnly) {
      const count = await dbService.count(model.schema, query);
      return res.success({ data: { totalRecords: count } });
    }

    if (dataToFind.options) {
      options = dataToFind.options;
    }

    if (dataToFind.customSearch) {
      const keys = Object.keys(dataToFind.customSearch);
      const orConditions = [];

      keys.forEach((key) => {
        if (typeof dataToFind.customSearch[key] === 'number') {
          orConditions.push(
            model.sequelize.where(model.sequelize.cast(model.sequelize.col(key), 'varchar'), {
              [Op.iLike]: `%${dataToFind.customSearch[key]}%`
            })
          );
        } else {
          orConditions.push({
            [key]: {
              [Op.iLike]: `%${dataToFind.customSearch[key]}%`
            }
          });
        }
      });

      if (orConditions.length > 0) {
        query[Op.or] = orConditions;
      }
    }

    const schemas = await dbService.smspaginate(model.schema, query, options);

    if (!schemas || schemas.length === 0) {
      return res.recordNotFound();
    }

    return res.status(200).send({
      status: 'SUCCESS',
      message: 'Your request is successfully executed',
      data: schemas.data,
      total: schemas.total
    });
  } catch (error) {
    console.error(error);
    return res.internalServerError({ message: error.message });
  }
};

